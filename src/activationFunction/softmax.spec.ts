import softmax from './softmax';

test('softmax', () => {
  const test = [[1, 1, 1], [2, 2, 2], [3, 3, 3], [4, 4, 4]];
  const ro = softmax(test);
  expect(ro.A).toEqual([
    [0.03205860328008499, 0.03205860328008499, 0.03205860328008499],
    [0.08714431874203257, 0.08714431874203257, 0.08714431874203257],
    [0.23688281808991016, 0.23688281808991016, 0.23688281808991016],
    [0.6439142598879722, 0.6439142598879722, 0.6439142598879722],
  ]);
});